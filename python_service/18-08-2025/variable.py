# Integer variable
age = 25
print("Age:", age)

# Float variable
price = 19.99
print("Price:", price)

# String variable
name = "Alice"
print("Name:", name)

# Boolean variable
is_student = True
print("Is student?", is_student)

# List variable
colors = ["red", "green", "blue"]
print("Colors:", colors)

# Tuple variable
point = (10, 20)
print("Point:", point)

# Dictionary variable
person = {"name": "Bob", "age": 30}
print("Person info:", person)

# Set variable
unique_numbers = {1, 2, 3}
print("Unique numbers:", unique_numbers)